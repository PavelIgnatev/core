phoneNumbers.forEach((number) => {
  // Форматируем номер: если он не начинается с "+", добавляем его
  const formattedNumber = number.startsWith('+') ? number : '+' + number;

  // Разбираем номер телефона
  const phoneNumber = parsePhoneNumberFromString(formattedNumber);

  let countryName;

  if (phoneNumber && phoneNumber.country) {
    // Получаем полное название страны по ISO-коду
    countryName = countries.getName(phoneNumber.country, 'en');
  } else {
    // Если определить страну не удалось, используем ключ "Unknown"
    countryName = 'Unknown';
  }

  // Если для страны ещё нет ключа в объекте, создаём массив
  if (!groupedNumbers[countryName]) {
    groupedNumbers[countryName] = [];
  }

  // Добавляем номер в соответствующий массив
  groupedNumbers[countryName].push(number);
});

